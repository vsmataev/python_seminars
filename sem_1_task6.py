"""Задача 6: Вы пользуетесь общественным транспортом? Вероятно, вы
расплачивались за проезд и получали билет с номером. Счастливым
билетом называют такой билет с шестизначным номером, где сумма
первых трех цифр равна сумме последних трех. Т.е. билет с номером
385916 – счастливый, т.к. 3+8+5=9+1+6. Вам требуется написать
программу, которая проверяет счастливость билета."""

ticket_number = input("Введите номер билета (6 цифр): ")

if len(ticket_number) != 6 or not ticket_number.isdigit():
    print("Пожалуйста, введите шестизначный номер билета, состоящий только из цифр.")
else:
    first_half = sum(map(int, ticket_number[:3]))  # Сумма первых трех цифр
    second_half = sum(map(int, ticket_number[3:]))  # Сумма последних трех цифр

    if first_half == second_half:
        print("У вас счастливый билет!")
    else:
        print("К сожалению, это не счастливый билет.")